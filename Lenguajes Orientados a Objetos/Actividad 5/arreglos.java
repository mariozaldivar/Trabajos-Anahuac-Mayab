class Arreglos 
{
	public static void main(String args[])
	{

		int[] arreglo = new int[10];

		System.out.println("Indice\tValor");

		for (int indice = 0; indice < arreglo.length; indice++)
		{
			System.out.println(indice+"\t"+arreglo[indice]);
		}

	}
}



